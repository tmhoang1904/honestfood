package vn.com.arillance.honestfood.model;

public class DistrictModel {
	private int districtId;
	private String districtName;
	private String districtPlace;
	private int districtPlaceId;
	private int serviceId;

	public int getDistrictId() {
		return districtId;
	}

	public void setDistrictId(int districtId) {
		this.districtId = districtId;
	}

	public String getDistrictName() {
		return districtName;
	}

	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}

	public String getDistrictPlace() {
		return districtPlace;
	}

	public void setDistrictPlace(String districtPlace) {
		this.districtPlace = districtPlace;
	}

	public int getDistrictPlaceId() {
		return districtPlaceId;
	}

	public void setDistrictPlaceId(int districtPlaceId) {
		this.districtPlaceId = districtPlaceId;
	}

	public int getServiceId() {
		return serviceId;
	}

	public void setServiceId(int serviceId) {
		this.serviceId = serviceId;
	}
	
	
}
